#Operadores aritiméticos
#para a adição, usamos o +
soma = 10 + 10
print('Soma:', soma)

#para a subtração usamos o -
subtracao = 15 - 12
print('Subtração:', subtracao)

#para a multiplicação usamos o *
multiplicacao = 5 * 2
print('Multiplicação:', multiplicacao)

#para a divisão usamos o /
divisao = 10 / 2
print('Divisão:', divisao)

#para a divisão de numeros inteiros, ou seja, caso seja numero com vírgula ele sera truncado
#usamos a //
divisao_inteiro = 15 // 2
print('Divisão Inteiro:', divisao_inteiro)

#para fazer exponencial, usamos **
exp = 2 ** 3
print('Exponencial:', exp)

#para tirarmos o resto da divisão, usamos o %
resto = 15 % 2
print('Resto da divisão:', resto)

#para descobrir se um numero é par, por exemplo, podemos usar os booleans
print (15 % 2 == 0) #false se for impar, logo, não é divisivel por dois
print (14 % 2 == 0) #true se for par, logo divisivel por 2
print(15 * 2 >= 30)