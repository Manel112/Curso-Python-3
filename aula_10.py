#para somarmos letras, por exemplo, fazer A+B = AB, nós usamos
#o sinal de mais, para fazer a concatenação e precisa estar entre ''

concatenacao = 'A' + 'B' + 'C'
print(concatenacao)

#podemos fazer isso com um numero tambem, caso transformemos ele para uma string
concatenacao2 = 'Manel' + ' ' + str(3) + 'mil' + 'vezes'
print(concatenacao2)

#para fazermos a repetição da mesma palavar varias vezes, usamos a multiplicação
#logo, o sinal *
repetir = 'Manel' * 20
print(repetir)